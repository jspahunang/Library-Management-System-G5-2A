export function getNavItemsForRole(role: UserRole): NavItem[] {
  const base = `/${role.toLowerCase()}`;
  switch (role) {
    case 'Admin':
      return [
        { label: 'Dashboard', route: base, icon: iconHome },
        { label: 'User Management', route: `${base}/users`, icon: iconUsers },
      ];
    case 'Librarian':
      return [
        { label: 'Dashboard', route: base, icon: iconHome },
        { label: 'Book Management', route: `${base}/books`, icon: iconBook },
        { label: 'Borrow / Return', route: `${base}/transactions`, icon: iconBorrow },
        { label: 'Fines', route: `${base}/fines`, icon: iconFine },
        { label: 'Notifications', route: `${base}/notifications`, icon: iconBell },
      ];
    case 'Student':
      return [
        { label: 'Dashboard', route: base, icon: iconHome },
        { label: 'Book Catalog', route: `${base}/catalog`, icon: iconBook },
        { label: 'My Borrowed Books', route: `${base}/borrowed`, icon: iconBorrow },
        { label: 'Fines', route: `${base}/fines`, icon: iconFine },
        { label: 'Notifications', route: `${base}/notifications`, icon: iconBell },
      ];
    case 'Teacher':
      return [
        { label: 'Dashboard', route: base, icon: iconHome },
        { label: 'Book Catalog', route: `${base}/catalog`, icon: iconBook },
        { label: 'Borrow History', route: `${base}/history`, icon: iconBorrow },
      ];
    default:
      return [];
  }
}
